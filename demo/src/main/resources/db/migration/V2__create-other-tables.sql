CREATE TABLE tire (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tire_brand VARCHAR(255) NOT NULL,
    pressure FLOAT NOT NULL,
    fire_number INT NOT NULL UNIQUE,
    status VARCHAR(20) NOT NULL
);

CREATE TABLE vehicle_positions (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id BIGINT NOT NULL,
    tire_id BIGINT NOT NULL,
    position VARCHAR(255) NOT NULL,
    CONSTRAINT fk_vehicle FOREIGN KEY (vehicle_id) REFERENCES vehicle(id) ON DELETE CASCADE,
    CONSTRAINT fk_tire FOREIGN KEY (tire_id) REFERENCES tire(id) ON DELETE CASCADE
);